package com.example.demo.model;

import javax.persistence.Entity;

import javax.persistence.Column;

import javax.persistence.GenerationType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;


@Entity
@Table(name = "Volunteer")
public class Volunteer {
	
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private int V_ID;
	
	@Column(name = "V_FNAME")
	private String V_FNAME;
	
	@Column(name = "V_LNAME")
	private String V_LNAME;
	
	@Column(name = "V_BLOOD_GROUP")
	private String V_BLOOD_GROUP;
	
	@Column(name = "V_CONTACT")
	private String V_CONTACT;
	
	@Column(name = "V_ADDRESS")
	private String V_ADDRESS;
	
	@Column(name = "V_CITY_NAME")
	private String V_CITY_NAME;
	
	@Column(name = "V_EMAIL")
	private String V_EMAIL;
	
	@Column(name = "V_TypeOfWork")
	private String V_TypeOfWork;
	
	@Column(name = "V_USER_ID")
	private String V_USER_ID;
	
	@Column(name = "V_PASSWORD")
	private String V_PASSWORD;
	
	
	public Volunteer()
	{
	
	}


	public Volunteer(int v_ID, String v_FNAME, String v_LNAME, String v_BLOOD_GROUP, String v_CONTACT, String v_ADDRESS,
			String v_CITY_NAME, String v_EMAIL, String v_TypeOfWork, String v_USER_ID, String v_PASSWORD) {
		super();
		V_ID = v_ID;
		V_FNAME = v_FNAME;
		V_LNAME = v_LNAME;
		V_BLOOD_GROUP = v_BLOOD_GROUP;
		V_CONTACT = v_CONTACT;
		V_ADDRESS = v_ADDRESS;
		V_CITY_NAME = v_CITY_NAME;
		V_EMAIL = v_EMAIL;
		V_TypeOfWork = v_TypeOfWork;
		V_USER_ID = v_USER_ID;
		V_PASSWORD = v_PASSWORD;
	}


	public int getV_ID() {
		return V_ID;
	}


	public void setV_ID(int v_ID) {
		V_ID = v_ID;
	}


	public String getV_FNAME() {
		return V_FNAME;
	}


	public void setV_FNAME(String v_FNAME) {
		V_FNAME = v_FNAME;
	}


	public String getV_LNAME() {
		return V_LNAME;
	}


	public void setV_LNAME(String v_LNAME) {
		V_LNAME = v_LNAME;
	}


	public String getV_BLOOD_GROUP() {
		return V_BLOOD_GROUP;
	}


	public void setV_BLOOD_GROUP(String v_BLOOD_GROUP) {
		V_BLOOD_GROUP = v_BLOOD_GROUP;
	}


	public String getV_CONTACT() {
		return V_CONTACT;
	}


	public void setV_CONTACT(String v_CONTACT) {
		V_CONTACT = v_CONTACT;
	}


	public String getV_ADDRESS() {
		return V_ADDRESS;
	}


	public void setV_ADDRESS(String v_ADDRESS) {
		V_ADDRESS = v_ADDRESS;
	}


	public String getV_CITY_NAME() {
		return V_CITY_NAME;
	}


	public void setV_CITY_NAME(String v_CITY_NAME) {
		V_CITY_NAME = v_CITY_NAME;
	}


	public String getV_EMAIL() {
		return V_EMAIL;
	}


	public void setV_EMAIL(String v_EMAIL) {
		V_EMAIL = v_EMAIL;
	}


	public String getV_TypeOfWork() {
		return V_TypeOfWork;
	}


	public void setV_TypeOfWork(String v_TypeOfWork) {
		V_TypeOfWork = v_TypeOfWork;
	}


	public String getV_USER_ID() {
		return V_USER_ID;
	}


	public void setV_USER_ID(String v_USER_ID) {
		V_USER_ID = v_USER_ID;
	}


	public String getV_PASSWORD() {
		return V_PASSWORD;
	}


	public void setV_PASSWORD(String v_PASSWORD) {
		V_PASSWORD = v_PASSWORD;
	}


	@Override
	public String toString() {
		return "Volunteer [V_ID=" + V_ID + ", V_FNAME=" + V_FNAME + ", V_LNAME=" + V_LNAME + ", V_BLOOD_GROUP="
				+ V_BLOOD_GROUP + ", V_CONTACT=" + V_CONTACT + ", V_ADDRESS=" + V_ADDRESS + ", V_CITY_NAME="
				+ V_CITY_NAME + ", V_EMAIL=" + V_EMAIL + ", V_TypeOfWork=" + V_TypeOfWork + ", V_USER_ID=" + V_USER_ID
				+ ", V_PASSWORD=" + V_PASSWORD + "]";
	}
	
	
	

}
